# Format integer
print("{0} bila diubah jadi biner manejadi {0:b}".format(12))

# Format Float
print("Format eksponensial: {0:e}".format(1566.345))

# Format Pembulatan
print("Sepertiga sama dengan : {0:.3f}".format(1/3))

# Meratakan string
print("|{:<10}|{:^10}|{:>10}|".format('beras','gula','garam'))
